i1 = int(input("Digite o primeiro número inteiro: "))
i2 = int(input("Digite o segundo número inteiro: "))
r = float(input("Digite um número real: "))
produto = (2 * i1) * (i2 / 2)
soma = (3 * i1) + r
terceiro = r ** 3
print ("O produto do dobro do primeiro com metade do segundo é %4.2f " % produto)
print ("A soma do triplo do primeiro com o terceiro é %4.2f " % soma)
print ("O terceiro elevado ao cubo é %4.2f " % terceiro)
